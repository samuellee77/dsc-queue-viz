<h2 class="body-header">Introduction</h2>
<p class='body-text'>
    The history of minimum wage legislation in the United States is a journey 
    marked by social advocacy, economic necessity, and political debate. 
    The concept of a minimum wage was introduced to ensure that workers could earn 
    a living wage, providing for their basic needs without falling into poverty. 
    Over time, this idea evolved, leading to significant legislative milestones.
    <br><br>
    The origins of minimum wage laws in the United States can be traced back to the early 20th century.
    The first state to introduce a minimum wage was Massachusetts in 1912, 
    aimed primarily at protecting women and children in the workforce.
    (<a href='https://www.history.com/news/minimum-wage-america-timeline' target="_blank">1</a>)
    This state-level initiative set a precedent that other states would follow in the ensuing years.
    <br><br>
    A pivotal moment in the history of minimum wage legislation came with the passage of 
    the Fair Labor Standards Act (FLSA) in 1938 under President Franklin D. Roosevelt's New Deal. (<a href='https://www.dol.gov/general/aboutdol/history/flsa1938#' target="_blank">2</a>)
    The FLSA established a federal minimum wage of 25 cents per hour, standardized the 40-hour workweek, 
    and mandated overtime pay. This legislation was a response to the economic hardships of the Great Depression, 
    aiming to boost consumer purchasing power and stimulate the economy.
</p>

<h2 class='body-header'>After WWII - Now</h2>
<p class='body-text'>
    In the post-World War II era, the federal minimum wage continued to rise, reflecting the nationâ€™s economic growth and inflation rates. 
    The 1960s and 1970s saw significant increases, driven by the civil rights movement and a growing recognition of income inequality.
    During this period, the minimum wage was raised several times, reaching $2.30 by 1976.
    <br><br>
    The 1980s and 1990s experienced a slower pace of minimum wage increases, partly due to differing political priorities and economic philosophies.
    However, the minimum wage debate regained momentum in the 2000s.
    The latest increase in federal minimum wage is from The Fair Minimum Wage Act of 2007, signed by President George W. Bush.
    It raised the federal minimum wage to $7.25 per hour, effective in 2009.
